        <p>They say that small is beautiful. In that case, the new Reykjavik loudspeaker must be qualified as simply stunning. Its ultra-compact, pebble-like design is just as intriguing and understated as the rough Icelandic nature that inspired this loudspeaker. “We wanted to complete our range of portable loudspeakers, which already includes Helsinki and Oslo,” says Michael Sørensen, CEO at Vifa: “As we have done with all our speakers, we wanted to challenge the physical limits for creating great sound. I must say that, once again, our engineering team solved the task in the most marvelous way. As tiny as it seems, Reykjavik still has an impressively powerful sound, due to clever and careful engineering.”</p>
        <p>Material-wise, there are certainly surprises too. One model still comes in the classic, high-quality Kvadrat woolen fabric that covers Vifa’s other products. But Reykjavik is also available in a rough, rocky version of anodized aluminum. A whole new surface to the brand’s universe and a tribute to the Icelandic muses: “The aluminum version carries a color called Lavastone Black. We did several prototypes before ending up with this specific metal cover in this particular color. We wanted to convey the fissured, foggy sceneries of the Northern hemisphere, and I do hope this new look will be just as well received as the more gentle looking version of Reykjavik in Sandstone Gray,” CEO Michael Sørensen says.</p>
        <p>The new surface material is sturdy and resistant and perfectly matches the bring-me-along existence of a portable product. The leather strap allows you to carry the loudspeaker with you or hang it wherever you want.</p>
        <p>Reykjavik is the fifth wireless loudspeaker launched by Vifa in only three years. The Danish manufacturer’s audio roots go all the way back to the 1930s, as a sub-supplier to other speaker brands.</p>
        <p>Believing that premium sound deserves a high-end presentation, in 2014 Vifa decided to launch its own range of wireless loudspeakers, building on the solid experience and proud craftsmanship behind the brand. The result is a series of high-end, highly intuitive, and aesthetically attractive loudspeakers, which never compromise the authentic sound that has been a core value for the brand throughout the years.</p>
        <p>“We are really proud that we’ve managed to create this piece, in line with our legacy of designing outstanding sound,” concludes Michael Sørensen, CEO at Vifa.</p>
